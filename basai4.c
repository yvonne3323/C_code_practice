//有 30 个数，分别为：99, 22, 51, 63, 72, 61, 20, 88, 40, 21, 63, 30, 11, 18, 99, 12, 93, 16, 7, 53, 64, 9, 28, 84, 34, 96, 52, 82, 51, 77 
//取出两个序号不同的数,两个数的乘积大于等于 2022 
#include <stdio.h>
int main()
{
    int a[30] = {99, 22, 51, 63, 72, 61, 20, 88, 40, 21, 63, 30, 11, 18, 99, 12, 93, 16, 7, 53, 64, 9, 28, 84, 34, 96, 52, 82, 51, 77};
    int i, j, k=0;
    for (i = 0; i < 30; i++)
    {
        for (j = 0; j < 30; j++)
        {
            if (i != j)
            {
                if (a[i] * a[j] >= 2022)
                {
                    printf("%d %d\n", a[i], a[j]);
                    k += 1;
                }
            }
        }
    }
    printf("共有 %d 对数满足条件", k/2);
    return 0;
}